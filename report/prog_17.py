for i in range(0, 10): # 0에서 9까지의 숫자 반복
    if(i % 3 == 0 and i % 5 == 0): # 3이랑 5 둘다 나눠떨어지는 경우를 최상단에위치 함.
        print("fizzbuzz") # 3이랑도, 5랑도 나누어 떨어질 경우
    elif(i % 3 == 0): # 이전 조건문에 반하는 elif 문이기에 결과식은 ("i%3==0 or i%5==0" 인 경우) -> 일경우중에, 3으로만 나누어질때
        print("fizz")
    elif(i % 5 == 0): # 5로만 나누어질때
        print("buzz")
    else: # 위 조건식 모두에 해당하지 않을경우
        print("*")